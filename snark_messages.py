def _build_messages() -> List[str]:
    combos: List[str] = []
    for index, (adj, action, twist) in enumerate(
        itertools.product(_ADJECTIVES, _ACTIONS, _TWISTS)
    ):
        if index >= 1000:
            break
        emoji = _EMOJI_SWIRL[index % len(_EMOJI_SWIRL)]
        combos.append(f"{adj} {action}; {twist}. {emoji}")
    return combos


